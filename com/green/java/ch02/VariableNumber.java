package com.green.java.ch02;

public class VariableNumber {
    public static void main(String[] args){
        byte b1; //1byte = 8bit
        short s1; //2byte
        long l1; //8byte
        int n1; //정수형 변수 선언 //4byte = 32bit
        n1 = 10; // =은 대입 연산자, 오른쪽 값 복사하여 왼쪽에 주입 = 초기화.
        System.out.println(n1);
        n1 = 20;
        System.out.println(n1);
    }
}
