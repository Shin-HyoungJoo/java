package com.green.java.ch03;

public class OperatorStudy6 {
    public static void main(String[] args) {
        int n1, n2, val = 10;

        n1 = n2 = val;              // n2 = val 부터 계산한다. 즉 오른쪽에서 왼쪽 순으로 계산함.

        System.out.println(n1);
        System.out.println(n2);
        System.out.println(val);
    }
}
